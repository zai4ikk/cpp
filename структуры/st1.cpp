/*Dynamic5◦. 
Дана вершина A1 непустого стека. Извлечь из стека первый (верх-
ний) элемент и вывести его значение D, а также ссылку A2 на новую
вершину стека. Если после извлечения элемента стек окажется пустым,
то положить A2 = null. После извлечения элемента из стека освободить
ресурсы, используемые этим элементом, вызвав его метод Dispose.*/

#include <iostream>
#include <stack>

int main() {
    std::stack<int> st; // создаем стек целых чисел
    st.push(10); // добавляем элементы в стек
    st.push(20);
    st.push(30);

    int D = st.top(); // извлекаем верхний элемент стека
    st.pop(); // удаляем верхний элемент стека

    if (st.empty()) { // проверяем, пуст ли стек
        std::cout << "A2 = null" << std::endl;
    } else {
        std::cout << "A2: " << &st.top() << std::endl; // выводим ссылку на новую вершину стека
    }

    std::cout << "D: " << D << std::endl; // выводим значение D

    // вызываем метод Dispose для освобождения ресурсов, используемых верхним элементом стека
    Dispose(D);

    return 0;
}